
/**
 * add - sum two int
 * @a: First value
 * @b: Second value
 *
 * Return: The sum
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - difference of two int
 * @a: first number
 * @b: second number
 *
 * Return: their differences
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - product of two int
 * @a: the first number
 * @b: the second number
 *
 * Return: their product
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - division of two ints
 * @a: the frist number
 * @b: the second number
 *
 * Return: integer division value
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - remainder of the result of two ints
 * @a: the first int
 * @b: the second int
 *
 * Return: Their remainder
 */
int mod(int a, int b)
{
	return (a % b);
}
